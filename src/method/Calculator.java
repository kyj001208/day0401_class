package method;

public class Calculator {

	// 기능
	// 더하기 기능

	public int plus(int a, int b) {
		return a + b; // rMethod eturn 사용시
	}

	// 빼기기능

	public int minus(int a, int b) {

		return a - b;
	}

	public int multifly(int a, int b) {

		return a * b;

	}

	public double division(int a, int b) {

		return (double) a / b;

	}

	public void println(byte n) {

		System.out.println(n);
	}

	public void println(char n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);
	}

	public void println(short n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);
	}

	public void println(int n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);
	}

	public void println(long n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);

	}

	public void println(float n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);
	}

	public void println(double n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);
	}

	public void println(String n) { // 매개 변수 또는 데이터 타입

		System.out.println(n);
	}
	
	public void println(String n, int a) { // 매개 변수 또는 데이터 타입

		System.out.println(n + a);
	}
	
	public void println(String a, String n) { // 매개 변수 또는 데이터 타입

		System.out.println(n + a);
	}

}
